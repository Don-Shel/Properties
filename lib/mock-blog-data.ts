export function getBlogPostBySlug(slug: string): BlogPost | undefined {
  return mockBlogPosts.find((post) => post.slug === slug)
}

export function getBlogPostById(id: string): BlogPost | undefined {
  return mockBlogPosts.find((post) => post.id === id)
}

export function getPublishedBlogPosts(): BlogPost[] {
  return mockBlogPosts.filter((post) => post.published).sort((a, b) => b.createdAt.getTime() - a.createdAt.getTime())
}

export function getBlogPostsByCategory(category: string): BlogPost[] {
  return getPublishedBlogPosts().filter((post) => post.category === category)
}

export function getUniqueBlogCategories(): string[] {
  const categories = new Set(mockBlogPosts.filter((post) => post.published).map((post) => post.category))
  return Array.from(categories).sort()
}
